package JavaBasics;

public class SuperConcept {
	

	public SuperConcept() {
		System.out.println("Parent Class Constructor");
	}
	public SuperConcept(int i) {
		System.out.println("Parent class constructor with value of i " + i);
	}
	public SuperConcept(int i, int j) {
		System.out.println("Parent class constructor with value of i " + i);
		System.out.println("Parent class constructor with value of j " + i);
	}
}
